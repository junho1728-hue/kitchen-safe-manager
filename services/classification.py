"""A등급(고위험군) 자동 분류 서비스.

규칙:
- 고기, 계란, 두부, 수산물 등 → A등급 (집중관리)
- 채소류 → 무조건 제외 (normal)
"""

# A등급 키워드 (고위험 식자재)
A_GRADE_KEYWORDS: list[str] = [
    # 육류
    "고기", "돼지", "소고기", "한우", "닭", "오리", "양고기", "육",
    "삼겹", "목살", "갈비", "안심", "등심", "차돌", "불고기",
    "닭가슴", "닭다리", "날개", "족발", "보쌈",
    # 계란
    "계란", "달걀", "란",
    # 두부
    "두부",
    # 수산물
    "수산", "생선", "회", "새우", "오징어", "조개", "굴",
    "어묵", "게", "연어", "참치", "광어", "우럭", "고등어",
    "삼치", "갈치", "꽃게", "대게", "랍스터", "전복", "멍게",
    "문어", "낙지", "주꾸미", "바지락", "홍합", "가리비",
    # 유제품
    "우유", "유제품", "치즈", "크림", "버터", "요거트", "요구르트",
]

# 제외 키워드 (채소류 — 관리 대상에서 완전 제외)
EXCLUDE_KEYWORDS: list[str] = [
    "야채", "채소", "양파", "감자", "당근", "배추", "무",
    "시금치", "상추", "깻잎", "고추", "파프리카", "브로콜리",
    "콩나물", "숙주", "버섯", "대파", "쪽파", "마늘", "생강",
    "고구마", "호박", "오이", "가지", "토마토", "양배추",
    "셀러리", "아스파라거스", "피망", "청경채", "미나리",
]


def classify_product(name: str) -> str:
    """제품명으로 등급 분류. 'A' 또는 'normal' 반환."""
    name = name.strip()

    # 1) 채소 제외 체크 (최우선)
    if any(kw in name for kw in EXCLUDE_KEYWORDS):
        return "normal"

    # 2) A등급 키워드 체크
    if any(kw in name for kw in A_GRADE_KEYWORDS):
        return "A"

    return "normal"
